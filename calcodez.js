//!!Default Functions

const appendChild = (arg1, arg2) => arg1.appendChild(arg2);
const getById = (arg1) => document.getElementById(arg1);
const getByClass = (arg) => document.getElementsByClassName(arg);
const addClassList = (arg1, arg2) => arg1.document.addClassList(arg2);
const removeClassList = (arg1, arg2) => arg1.document.removeClassList(arg2);
const toggleClassList = (arg1, arg2) => arg1.document.toggleClassList(arg2);
const createElement = (arg1) => document.createElement(arg1);
const selectAll = (arg) => document.querySelectorAll(arg);
const select = (arg) => document.querySelector(arg);
